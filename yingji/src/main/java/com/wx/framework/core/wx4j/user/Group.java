package com.wx.framework.core.wx4j.user;

public class Group
{
  private Integer id;
  private String name;
  private int count;

  public Integer getId()
  {
    return this.id; }

  public void setId(Integer id) {
    this.id = id; }

  public String getName() {
    return this.name; }

  public void setName(String name) {
    this.name = name; }

  public int getCount() {
    return this.count; }

  public void setCount(int count) {
    this.count = count;
  }
}