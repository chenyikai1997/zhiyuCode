package com.fh.entity.backend;

import java.util.List;

//监控点实体类
public class Monitors {
	private String MONITORTYPE_NAME;			//监控点分类名称
	private List<Monitor> MONITOR_LIST;			//监控点
	public String getMONITORTYPE_NAME() {
		return MONITORTYPE_NAME;
	}
	public void setMONITORTYPE_NAME(String mONITORTYPE_NAME) {
		MONITORTYPE_NAME = mONITORTYPE_NAME;
	}
	public List<Monitor> getMONITOR_LIST() {
		return MONITOR_LIST;
	}
	public void setMONITOR_LIST(List<Monitor> mONITOR_LIST) {
		MONITOR_LIST = mONITOR_LIST;
	}
	
}
